function OnGossip(pUnit, event, pPlayer)
local menu = 100
for i = 1,#option_data do
	if(option_data[i][1] == pUnit:GetEntry())then
		menu = option_data[i][3]
	end
end
pUnit:GossipCreateMenu(menu, pPlayer, 0)
for k = 1,#option_data do
	if(option_data[k][3] == menu and option_data[k][1] == pUnit:GetEntry())then
		pUnit:GossipMenuAddItem(option_data[k][4], option_data[k][5], option_data[k][2], 0)
	end
end
pUnit:GossipAddQuests(pPlayer)
pUnit:GossipSendMenu(pPlayer)
end

function OnSelect(pUnit, event, pPlayer, id, intid, code)
for i = 1,#option_data do
	if(intid == option_data[i][2] and option_data[i][1] == pUnit:GetEntry())then
		pUnit:GossipCreateMenu(option_data[i][6], pPlayer, 0)
		pUnit:GossipSendMenu(pPlayer)
	end
end
end

for a = 1,#option_data do
if(option_data[a][2] == 1)then
	RegisterUnitGossipEvent(option_data[a][1],1,OnGossip)
	RegisterUnitGossipEvent(option_data[a][1],2,OnSelect)
end
end
